    private void btnRegistrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarseActionPerformed
        //registramos el usuario pasando los datos de los inputs
       int codigo = Registro.comprobarRegistro(inputName.getText(),inputEmail.getText(),inputPassword.getText(),inputPassword2.getText(),inputPhone.getText());
       String mensaje = "";
       switch (codigo){
           case ERROR_CAMPOS_VACIOS:
               mensaje = "Error, rellene todos los campos";
               break;
           case ERROR_NOMBRE:
               mensaje = "Error, el nombre no es valido";
               break;
           case ERROR_EMAIL:
               mensaje = "Error, el email no es valido";
                break;
           case ERROR_FORMATO_PASSWORD:
               mensaje = "Error, la contraseña debe tener minino 8 caracteres y un numero";
               break;
           case ERROR_PASSWORDS:
               mensaje = "Error, las contraseñas no coinciden";
               break;
           case ERROR_TELEFONO:
               mensaje = "Error, el telefono no es correcto";
               break;
           case INSERTADO:
               mensaje = "Usuario registrado correctamente";
               break;
       }
       //mostramos el mensaje correspondiente
       if(codigo < 0){
           JOptionPane.showMessageDialog(null,mensaje,"Error",JOptionPane.ERROR_MESSAGE);
       }else{
           JOptionPane.showMessageDialog(null,mensaje,"Correcto",JOptionPane.INFORMATION_MESSAGE);
       }
    }//GEN-LAST:event_btnRegistrarseActionPerformed

