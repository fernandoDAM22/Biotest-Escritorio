    /**
     * Este metodo permite realizar la funcion de login cuando se pulsa
     * la tecla enter teniendo el foco el campo de la password o del username
     * @param evt es la tecla que se pulsa
     * @author Fernando
     */
    private void pressEnter(KeyEvent evt) {
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            btnLogin.doClick();
        }
    }

    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {
        //guardamos el nombre porque lo necesitamos despues;
        String nombre = inputUsername.getText();
        int resultado = Login.login(nombre,inputPassword.getText()); //le pasamos los datos al login
        String mensaje = "";
        //en caso de error mostramos el mensaje correspondiente
        if(resultado == ERROR_NO_EXISTE_USUARIO){
            mensaje = Mensajes.NO_EXISTE_USUARIO;
        }else if(resultado == Codigos.ERROR_PASSWORD_INCORRECTA){
            mensaje = Mensajes.PASSWORD_INCORRECTA;
        }
        //mostramos el mensaje de error en caso de que haya ocurrido
        if(resultado < 0){
            JOptionPane.showMessageDialog(null,mensaje,Mensajes.ERROR,JOptionPane.ERROR_MESSAGE);
        }
        //si no ha habido errores lanzamos la ventana de seleccionar modo de juego
        if(resultado == CORRECTO){
            //guardamos el tipo del usuario conectado
            ConfiguracionUsuario.setTipoUsuario(Login.obtenerDatos(nombre,Codigos.OBTENER_TIPO));
            //guardamos el nombre del usuario conectado
            ConfiguracionUsuario.setNombreUsuario(nombre);
            //registramos en el log el usurario y la fecha en la que se conecto
            LocalDateTime fecha = LocalDateTime.now();
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd:HH:mm:ss");
            logger.info("El usuario " + ConfiguracionUsuario.getNombreUsuario() + " Inicio sesion en la fecha: " + formatter.format(fecha));
            //lanzamos la ventana seleccionar modo de juego
            VentanaSeleccionarModoJuego frame = new VentanaSeleccionarModoJuego();
            frame.setVisible(true);
            dispose();
        }
    }                                        

